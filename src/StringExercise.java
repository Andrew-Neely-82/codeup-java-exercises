public class StringExercise {
  public static void main(String[] args) {

    // For each of the following output examples: create a String variable that
    // contains the desired output and print it out to the console, you can do this
    // with only one String variable and one print statement for each output
    // example.

    // We don't need no education
    // We don't need no thought control

    // Check "this" out!, "s inside of "s!

    // In windows, the main drive is usually C:\

    // I can do backslashes \, double backslashes \\,
    // and the amazing triple backslash \\\!

    String noEducation = "We don't need no education";
    String noThoughtControl = "We don't need no thought control";
    String checkThisOut = "Check \"this\" out!, \"s inside of \"s!";
    String windows = "In windows, the main drive is usually C:\\";
    String backslashes = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";

    System.out.println(noEducation);
    System.out.println(noThoughtControl);
    System.out.println(checkThisOut);
    System.out.println(windows);
    System.out.println(backslashes);

  }
}
